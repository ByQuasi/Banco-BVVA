    private void modificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modificarActionPerformed
        String nombre = txt_nombre.getText().trim();
        String mail = txt_mail.getText().trim();
        if (txt_pass.getPassword().length == 0 && nombre.equals("") && mail.equals("")) {
            JOptionPane.showMessageDialog(null, "No hay cambios detectados");
            return;
        }
        Connection cn = c.getConnection();
        prepareUser(cn);
        try {
            PreparedStatement pst = cn.prepareStatement("update user_data set user_name = ?, user_mail = ?, user_pass = ? where user_user = ?");
            if (nombre.equals("")) {
                pst.setString(1, user_name);
            } else {
                pst.setString(1, nombre);
            }
            if (mail.equals("")) {
                pst.setString(2, user_mail);
            } else {
                pst.setString(2, mail);
            }
            if (txt_pass.getPassword().length == 0) {
                pst.setString(3, user_pass);
            } else {
                pst.setString(3, new String(txt_pass.getPassword()));
            }
            pst.setString(4, user);
            pst.executeUpdate();
            
            JOptionPane.showMessageDialog(null, "Modificacion exitosa");
            exitazo.setText("Modificacion exitosa");
            txt_nombre.setText("");
            txt_mail.setText("");
            txt_pass.setText("");
            txt_rpass.setText("");
            pst.close();
            cn.close();
        } catch (Exception e) {
            System.out.println("Error AB: " + e);
            JOptionPane.showMessageDialog(null, "No hay conexion");
        }

    }//GEN-LAST:event_modificarActionPerformed

