    public void getUser(String user) {
        this.user = user;
        Connection cn = c.getConnection();
        prepareUser(cn);
        try {
            cn.close();
        } catch (Exception e) {
        }
        
    }
    
    private void prepareUser(Connection cn) {
        try {
            PreparedStatement pst = cn.prepareStatement("select * from user_data where user_user = ?");
            pst.setString(1, user);
            ResultSet rs = pst.executeQuery();
            if (rs.next()) {
                id = rs.getInt("ID");
                user_name = rs.getString("user_name");
                user_mail = rs.getString("user_mail");
                user_pass = rs.getString("user_pass");
            }
            pst.close();
            rs.close();
        } catch (Exception e) {
            System.out.println("Erorr en prepareUserAA: " + e);
        }
    }
