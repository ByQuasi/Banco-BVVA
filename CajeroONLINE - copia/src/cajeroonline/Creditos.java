    public void getUser(String user) {
        this.user = user;
        Connection cn = c.getConnection();
        prepareUser(cn);
        try {
            cn.close();
        } catch (SQLException ex) {
            Logger.getLogger(Creditos.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void prepareUser(Connection cn) {
        try {
            PreparedStatement pst = cn.prepareStatement("select * from user_data where user_user = ?");
            pst.setString(1, user);
            ResultSet rs = pst.executeQuery();
            if (rs.next()) {
                id = rs.getInt("ID");
                plata = rs.getDouble("money");
            }
            setTable(cn);
            rs.close();
            pst.close();
        } catch (Exception e) {
            System.out.println("Error en prepareUser: " + e);
        }
    }

