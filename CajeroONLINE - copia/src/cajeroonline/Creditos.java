    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (plata >= palta && palta != 0) {
            Connection cn = c.getConnection();
            try {
                int unique_id = 0;

                java.util.Date utilDate = new java.util.Date(); // Fecha actual
                java.sql.Date sqlDate = new java.sql.Date(utilDate.getTime());
                PreparedStatement ps = cn.prepareStatement("insert into pagos_data values(?,?,?,?,?,?)");
                unique_id++;
                for (int i = 0; i < pagar.size(); i++) {
                    ps.setInt(1, id);
                    ps.setDouble(2, pagar.get(i));
                    ps.setInt(3, 3);
                    ps.setInt(4, ids.get(i));
                    ps.setString(5, "0");
                    ps.setDate(6, sqlDate);
                    ps.addBatch();
                    unique_id++;
                }
                ps.executeBatch();

                PreparedStatement pst = cn.prepareStatement("update user_data set money = money - ? where id = ?");
                pst.setDouble(1, palta);
                pst.setInt(2, id);
                pst.executeUpdate();
                palta = 0;
                updateCrediticio();
                setTable(cn);
                JOptionPane.showMessageDialog(null, "Pago exitoso!");
                pst.close();
                cn.close();
            } catch (Exception e) {
                try {
                    cn.rollback();
                } catch (SQLException ex) {
                    ex.printStackTrace();
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "No posee fondos necesarios");
        }
    }//GEN-LAST:event_jButton1ActionPerformed
